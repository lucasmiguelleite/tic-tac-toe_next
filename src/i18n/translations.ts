export const translate = (
  locale: Locale,
  key: string,
  params?: Record<string, string>,
) => {
  let text = translations[locale]?.[key] || translations.en[key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(`{${k}}`, v);
    }
  }
  return text;
};
